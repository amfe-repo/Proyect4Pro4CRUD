    private void btn_SignInMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_SignInMouseClicked
        systemConf sys = new systemConf();
           
        if(this.validateTxtFields()){
            JOptionPane.showMessageDialog(null, "Campos vacios");
        }else try(ResultSet rs = sys.selectData(generateSQLCode())) {
            
            if(!rs.isClosed()){
                
                sys.createSession(generateSession(rs));
                
                Principal pView = new Principal();
                
                lg.setVisible(false);
                
                pView.setVisible(true);
                
                rs.close();
                
            }else{
                JOptionPane.showMessageDialog(null, "El usuario no existe");
            }
        } catch (SQLException ex) {
            Logger.getLogger(LoginPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {
            sys.closeConnection();
        } catch (SQLException ex) {
            Logger.getLogger(LoginPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btn_SignInMouseClicked
    
    private Users generateSession(ResultSet info) throws SQLException{
        return new Users(
                Integer.parseInt(info.getString("id")),
                info.getString("name"),
                info.getString("lastname"),
                info.getString("username"),
                info.getString("phone"),
                info.getString("email")
        );
    }
    
    private String generateSQLCode(){
        
        String pass = new String(txt_pass.getPassword());
        
        String sql = String.format("select * from users where username = '%s' and password = '%s'",
                txt_username.getText(), pass);
        
        return sql;
    }
    
